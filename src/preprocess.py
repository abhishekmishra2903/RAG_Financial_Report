# Utility functions

def read_csv_safely(filename: str) -> pd.DataFrame:
    """
    Read CSV from data/raw folder.
    If file is missing or empty, return an empty DataFrame.
    """
    path = OUTPUT_DIR / filename

    if not path.exists():
        print(f"File not found: {path}")
        return pd.DataFrame()

    try:
        df = pd.read_csv(path)
        print(f"Loaded {filename}: {df.shape}")
        return df
    except pd.errors.EmptyDataError:
        print(f"Empty file: {filename}")
        return pd.DataFrame()
    except Exception as e:
        print(f"Error reading {filename}: {e}")
        return pd.DataFrame()


def clean_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    """
    General cleaning:
    - remove fully empty rows/columns
    - remove duplicate columns
    - strip column names
    """
    if df.empty:
        return df

    df = df.copy()

    df.columns = [str(col).strip() for col in df.columns]

    df = df.dropna(axis=0, how="all")
    df = df.dropna(axis=1, how="all")
    df = df.loc[:, ~df.columns.duplicated()]

    return df


def select_existing_columns(df: pd.DataFrame, columns: list[str]) -> pd.DataFrame:
    """
    Select only columns that exist in the DataFrame.
    This avoids KeyError if FMP response columns differ.
    """
    if df.empty:
        return df

    existing_columns = [col for col in columns if col in df.columns]

    if not existing_columns:
        return df

    return df[existing_columns].copy()


def convert_numeric_columns(df: pd.DataFrame, exclude_columns: list[str] | None = None) -> pd.DataFrame:
    """
    Try converting suitable columns to numeric.

    Uses errors='coerce' only when the column appears numeric enough.
    This avoids pandas errors and avoids converting text columns into NaN.
    """
    if df.empty:
        return df

    if exclude_columns is None:
        exclude_columns = []

    df = df.copy()

    for col in df.columns:
        if col in exclude_columns:
            continue

        original_series = df[col]
        converted_series = pd.to_numeric(original_series, errors="coerce")

        if converted_series.notna().sum() > 0:
            df[col] = converted_series

    return df


def save_processed_csv(df: pd.DataFrame, filename: str):
    """
    Save processed DataFrame to data/processed.
    """
    path = PROCESSED_DIR / filename
    df.to_csv(path, index=False)
    print(f"Saved processed CSV: {path}")


def prepare_for_sqlite(df: pd.DataFrame) -> pd.DataFrame:
    """
    Convert unsupported pandas/numpy objects into SQLite-friendly values.
    Especially handles Timestamp values in normal columns and object columns.
    """
    if df.empty:
        return df

    df = df.copy()

    for col in df.columns:
        if pd.api.types.is_datetime64_any_dtype(df[col]):
            df[col] = df[col].astype(str)

        elif df[col].dtype == "object":
            df[col] = df[col].apply(
                lambda x: x.isoformat() if isinstance(x, pd.Timestamp) else x
            )

    return df


def save_to_sqlite(tables: dict[str, pd.DataFrame]):
    """
    Save processed DataFrames into SQLite database.
    Converts Timestamp values before saving.
    """
    conn = sqlite3.connect(DB_PATH)

    for table_name, df in tables.items():
        sqlite_ready_df = prepare_for_sqlite(df)

        sqlite_ready_df.to_sql(
            table_name,
            conn,
            if_exists="replace",
            index=False
        )

        print(f"Saved table '{table_name}' to SQLite. Shape: {sqlite_ready_df.shape}")

    conn.close()
    print(f"SQLite database saved at: {DB_PATH}")


