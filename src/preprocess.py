    if pd.notna(total_debt) and pd.notna(cash):
        computed_metrics["net_debt"] = total_debt - cash

    if pd.notna(total_assets) and pd.notna(total_liabilities):
        computed_metrics["asset_liability_gap"] = total_assets - total_liabilities

    computed_metrics["latest_balance_sheet_date"] = latest_balance.get("date")


